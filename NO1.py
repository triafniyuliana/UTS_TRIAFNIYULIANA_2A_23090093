bilangan_pertama = float(input("masukan bilangan pertama :"))
bilangan_kedua =float(input("masukan bilangan ke dua :"))

hasil_penjumlahan = bilangan_pertama + bilangan_kedua
print("Hasil penjumlahan:", hasil_penjumlahan)

hasil_pengurangan = bilangan_pertama - bilangan_kedua
print("Hasil pengurangan:", hasil_pengurangan)

hasil_modulus = bilangan_pertama % bilangan_kedua
print("Hasil modulus:", hasil_modulus)